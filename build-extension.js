#!/usr/bin/env node

/**
 * Extension Build Script
 * Generates extension/config.js with backend URL from environment variables or .env file
 * 
 * Usage:
 *   node build-extension.js
 *   BACKEND_URL=http://example.com node build-extension.js
 *   python build-extension.py (if Node.js not available)
 */

const fs = require('fs');
const path = require('path');
const dotenv = require('dotenv');

// Get the extension directory
const extensionDir = path.join(__dirname, 'extension');
const envPath = path.join(extensionDir, '.env');
const configOutputPath = path.join(extensionDir, 'config.js');
const manifestPath = path.join(extensionDir, 'manifest.json');
const manifestTemplatePath = path.join(extensionDir, 'manifest.template.json');
const backgroundJsPath = path.join(extensionDir, 'background.js');

console.log('🔨 Building extension configuration...');

// Load environment variables
let config = {
  BACKEND_URL: 'http://localhost:5000',
  ENV_MODE: 'development'
};

// Try to load from .env file
if (fs.existsSync(envPath)) {
  console.log(`📁 Loading from ${envPath}`);
  const envConfig = dotenv.parse(fs.readFileSync(envPath));
  config = { ...config, ...envConfig };
} else {
  console.log(`⚠️  .env file not found at ${envPath}, using defaults`);
}

// Override with environment variables (takes precedence)
if (process.env.BACKEND_URL) config.BACKEND_URL = process.env.BACKEND_URL;
if (process.env.ENV_MODE) config.ENV_MODE = process.env.ENV_MODE;

// Validate backend URL
if (!config.BACKEND_URL) {
  console.error('❌ Error: BACKEND_URL not configured');
  process.exit(1);
}

// Remove trailing slash if present
config.BACKEND_URL = config.BACKEND_URL.replace(/\/$/, '');

console.log(`✓ Backend URL: ${config.BACKEND_URL}`);
console.log(`✓ Environment: ${config.ENV_MODE}`);

// Generate config.js
const configContent = `/**
 * AUTO-GENERATED FILE - DO NOT EDIT
 * Generated by build-extension.js
 * To change values, edit extension/.env or set BACKEND_URL environment variable
 */

const EXTENSION_CONFIG = {
  BACKEND_URL: '${config.BACKEND_URL}',
  ENV_MODE: '${config.ENV_MODE}',
  API_ENDPOINTS: {
    LOG_VIOLATION: '/log',
    SESSION: '/session',
    DASHBOARD: '/dashboard'
  }
};

// Export for use in background.js and popup.js
if (typeof module !== 'undefined' && module.exports) {
  module.exports = EXTENSION_CONFIG;
}
`;

fs.writeFileSync(configOutputPath, configContent);
console.log(`✅ Generated ${configOutputPath}`);

// Update background.js to inject the backend URL for service worker
try {
  let backgroundJs = fs.readFileSync(backgroundJsPath, 'utf8');
  
  // Replace the getDefaultBackendUrl function to return the configured URL
  const newGetDefaultBackendUrl = `const getDefaultBackendUrl = () => '${config.BACKEND_URL}';`;
  
  // Find and replace the getDefaultBackendUrl function definition
  backgroundJs = backgroundJs.replace(
    /const getDefaultBackendUrl = \(\) => \{[\s\S]*?\n\};/,
    newGetDefaultBackendUrl
  );
  
  fs.writeFileSync(backgroundJsPath, backgroundJs);
  console.log(`✅ Updated ${backgroundJsPath} with configured backend URL`);
} catch (error) {
  console.warn(`⚠️  Could not update background.js: ${error.message}`);
}

// Update manifest.json with correct host permissions
const hostPermissions = [
  'http://127.0.0.1/*',
  'http://localhost/*',
  config.BACKEND_URL + '/*'
];

try {
  let manifestContent;
  
  // Check if template exists, use it; otherwise read existing manifest
  if (fs.existsSync(manifestTemplatePath)) {
    manifestContent = JSON.parse(fs.readFileSync(manifestTemplatePath, 'utf8'));
    console.log(`📋 Using manifest template: ${manifestTemplatePath}`);
  } else {
    manifestContent = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
    console.log(`📋 Reading existing manifest: ${manifestPath}`);
  }
  
  // Update host permissions (in host_permissions array under permissions)
  if (manifestContent.host_permissions) {
    manifestContent.host_permissions = hostPermissions;
  }
  
  // Also update content_scripts default host if it exists
  if (manifestContent.content_scripts) {
    // Keep content_scripts as is but make a note
    console.log(`📝 Content scripts remain unchanged`);
  }
  
  fs.writeFileSync(manifestPath, JSON.stringify(manifestContent, null, 2));
  console.log(`✅ Updated ${manifestPath}`);
  console.log(`   Host permissions: ${hostPermissions.join(', ')}`);
} catch (error) {
  console.error(`❌ Error updating manifest: ${error.message}`);
  process.exit(1);
}

console.log('\n✨ Extension build complete!');
console.log('\nNext steps:');
console.log('1. Load the extension in Chrome: chrome://extensions/ > Load unpacked > select extension/');
console.log(`2. The extension will connect to: ${config.BACKEND_URL}`);
